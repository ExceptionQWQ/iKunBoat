void HandleDebugSerialMessage(const char* message, int len)
{
    switch (message[0]) {
        case 'w':
            MoveForward();
            break;
        case 's':
            MoveBackward();
            break;
        case 'a':
            TurnLeft();
            break;
        case 'd':
            TurnRight();
            break;
        case 'x':
            TurnMid();
            break;
        case 'p':
            Stop();
            break;
    }
}

void HandleWiFiSerialMessage(const char* message, int len)
{
    switch (message[0]) {
        case 'w':
            upFlags = 100;
            break;
        case 's':
            downFlags = 100;
            break;
        case 'a':
            leftFlags = 100;
            break;
        case 'd':
            rightFlags = 100;
            break;
    }
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef* huart)
{
    static uint8_t debugUartRecvBuff[1] = {};
    static uint8_t debugUartBuff[16] = {};
    static int debugUartOffset = 0;
    static uint8_t echoEnd[] = "\r\n";

    static uint8_t wifiRecvBuff[1] = {};


    if (huart->Instance == USART1) { //WiFi模块
//        wifiRecvBuff[0] = 'a';
//        HAL_UART_Transmit(&huart2, wifiRecvBuff, 1, 10); //数据回显
        HandleWiFiSerialMessage(wifiRecvBuff,  1);
        HAL_UART_Receive_IT(huart, wifiRecvBuff, 1);
    } else if (huart->Instance == USART2) { //调试
        debugUartBuff[debugUartOffset] = debugUartRecvBuff[0];
        if (debugUartBuff[debugUartOffset] == ';') {
            HAL_UART_Transmit(huart, debugUartBuff, debugUartOffset + 1, 10); //数据回显
            HAL_UART_Transmit(huart, echoEnd, strlen(echoEnd), 10); //数据回显
            HandleDebugSerialMessage(debugUartBuff, debugUartOffset + 1);
            debugUartOffset = 0;
        } else {
            ++debugUartOffset;
        }
        HAL_UART_Receive_IT(huart, debugUartRecvBuff, 1);
    }
}



